void	rush(int x, int y)
{
	int	i;
	int	j;

	i = 1;
	j = 1;
	while (j <= y && y > 0 && x > 0)
	{
		i = 1;
		while (i <= x)
		{
			ft_putchar(i, j, x, y);
			i++;
		}
		write(1, "\n", 1);
		j++;
	}
}
